    public static void updatePassword(String newPassword) {
        String[] adminDetails = logintest.getuserDetails();
        if (adminDetails != null && adminDetails.length > 4) {
            adminDetails[4] = newPassword;
            logintest.setuserDetails(adminDetails); 
        }}
   
